<template>
<transition v-if="show" name="modal">
  <div class="modal-mask">
    <div class="modal-container">
      <form class="pure-form" @submit.prevent="upload">
        <legend>Insert Some Numbers</legend>
        <fieldset>
          <input v-model="number1" placeholder="Enter a number">
        </fieldset>
        <fieldset>
          <input v-model="number2" placeholder="Enter another number">
        </fieldset>
        <fieldset>
          <input v-model="operator" placeholder="Enter an operator">
        </fieldset>
        <!--
        <fieldset>
          <div class="imageInput" @click="chooseImage">
            <img v-if="url" :src="url" />
            <div v-if="!url" class="placeholder">
              Choose an Image
            </div>
            <input class="fileInput" ref="fileInput" type="file" @input="fileChanged">
          </div>
          <p v-if="error" class="error">{{error}}</p>
        </fieldset>
        -->
        <fieldset class="buttons">
          <button type="button" @click="close" class="pure-button">Close</button>
          <button type="submit" class="pure-button pure-button-primary right">Upload</button>
        </fieldset>
      </form>
    </div>
  </div>
</transition>
</template>


<script>
import axios from 'axios';
export default {
  name: 'AddNumber',
  props: {
    show: Boolean,
  },
  data() {
    return {
      number1: 0,
      number2: 0,
      operator: '',
      error: '',
    }
  },
  methods: {

    close() {
        this.$emit('close');
    },

    async upload() {
      try {
        const formData = new FormData();
        formData.append('number1', this.number1);
        formData.append('number2', this.number2);
        formData.append('operator', this.operator);
        await axios.post("/api/numbers", formData);
        this.number1 = 0;
        this.number2 = "0;";
        this.operator = "";
        this.$emit('uploadFinished');
      } catch (error) {
        this.error = "Error: " + error.response.data.message;
      }
    }
}
}
</script>


